# Farmer Profile
# -------------------------------------------------------

class FarmerProfile(models.Model):

    FARMING_PLACES = [
        ('pond', 'Pond'),
        ('lake', 'Lake'),
        ('cage', 'Cage'),
        ('other', 'Other'),
    ]

    FISH_SPECIES = [
        ('tilapia', 'Tilapia'),
        ('nile perch', 'Nile Perch'),
        ('trout', 'Trout'),
        ('catfish', 'Catfish'),
        ('other', 'Other'),
    ]

    AGE_GROUPS = [
        ('fingerlings', 'Fingerlings'),
        ('juvenile', 'Juvenile'),
        ('matured fish', 'Matured Fish'),
    ]

    user = models.OneToOneField(
        User,
        on_delete=models.CASCADE,
        related_name='farmer_profile'
    )

    # Location
    county = models.CharField(max_length=100)
    subcounty = models.CharField(max_length=100)

    # Farming Info
    place_of_farming = models.CharField(max_length=50, choices=FARMING_PLACES)
    fish_species = ArrayField(
        models.CharField(max_length=50, choices=FISH_SPECIES),
        default=list,
        blank=True
    )
    age_group = models.CharField(max_length=50, choices=AGE_GROUPS)

    date_registered = models.DateTimeField(default=timezone.now)

    def __str__(self):
        return f"{self.user.email} - {self.fish_species} ({self.place_of_farming})"


